    public void mostrarProveedores(Lista proveedores) {
        
         Iterador iteradorProveedores=proveedores.crearIterador();
        
        while(iteradorProveedores.siguiente()){
         Proveedor proveedor=(Proveedor)iteradorProveedores.getElementoActual();
         
         this.lstModelProveedor.addElement(proveedor.getRazonSocial());
         

        }
        
        
    }

    public void mostrarPedidosDeProveedor(Lista pedidos) {
        
        if(pedidos.estaVacia()){
         mostrarMensajePantalla("No existen pedidos para el proveedor seleccionado",Mensaje.TIPO_AVISO);
         this.dtmPedidos.setRowCount(0);
         this.deshabilitarDatosPedido();
         return;
        }
        
        
        this.limpiarTablaPedidos();
        
        
        Iterador iteradorPedidos=pedidos.crearIterador();
        int fila=0;
        
        
        
        this.dtmPedidos.setRowCount(iteradorPedidos.getCantidadElementos());
        
        while(iteradorPedidos.siguiente()){
         PedidoAProveedor pedido=(PedidoAProveedor) iteradorPedidos.getElementoActual();
         
         this.tblPedidos.setValueAt(new Integer(pedido.getNumeroPedido()), fila, 0);
         this.tblPedidos.setValueAt(ValText.getFormatStringFecha(pedido.getFechaRealizacion()), fila, 1);
         this.tblPedidos.setValueAt(pedido.getHoraRealizacion(), fila, 2);
         this.tblPedidos.setValueAt(pedido.getEstado().getNombre(), fila, 3);
         
         if(pedido.getRepresentanteProveedor().getApellido().compareTo("")==0 && pedido.getRepresentanteProveedor().getNombre().compareTo("")==0){
           this.tblPedidos.setValueAt("No Posee", fila, 4);
         }
         else{
            this.tblPedidos.setValueAt(pedido.getRepresentanteProveedor().getApellido()+" "+pedido.getRepresentanteProveedor().getNombre(), fila, 4);
         }
         
         this.tblPedidos.setValueAt(new Double(pedido.getMontoTotal()), fila, 5);
         
         fila++;   
        }
        
        this.tblPedidos.setShowGrid(true);
        this.habilitarDatosPedido();
    }

