    private void cargarTablaProveedores()
    {
        PostgreSQLManager pg=new PostgreSQLManager();
        Connection cn=null;
        try {
            cn = pg.concectGetCn();
            filasProveedorXMateriaPrima=AccessViews.listProveedorXMateriaPrima(idMateriaPrima, cn);
            tblProvXMatPrima.updateUI();
        } catch (Exception ex) {
            Logger.getLogger(SeleccionarProveedor.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally
        {
            try {
                pg.disconnect();
            } catch (SQLException ex) {
                Logger.getLogger(SeleccionarProveedor.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    private void setearDatos()
    {
        Combo.setItemComboSeleccionado(cmbTipoMaterial, matprimaDB.getTipomaterial());
        lblNombre.setText(matprimaDB.getNombre());
        lblNroMateriaPrima.setText(NumerosAMostrar.getNumeroString(NumerosAMostrar.NRO_MATERIAPRIMA, idMateriaPrima));
        txtDescripcion.setText(matprimaDB.getDescripcion());
    }
    private void buscarMateriaPrima(long id)
    {
        PostgreSQLManager pg=new PostgreSQLManager();
        Connection cn=null;
        try {
            cn = pg.concectGetCn();
            matprimaDB=AccessMateriaPrima.findById(id, cn);
        } catch (Exception ex) {
            Logger.getLogger(SeleccionarProveedor.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally
        {
            try {
                pg.disconnect();
            } catch (SQLException ex) {
                Logger.getLogger(SeleccionarProveedor.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    private void cargarComboTipoMaterial()
    {
        PostgreSQLManager pg=new PostgreSQLManager();
        Connection cn=null;
        TipomaterialDB[] vDB=null;
        try {
            cn = pg.concectGetCn();
            vDB=AccessTipoMaterial.findAll(cn);
            for(int i=0;i<vDB.length;i++)
            {
                Combo.cargarCombo(cmbTipoMaterial,String.valueOf(vDB[i].getIdtipomaterial()),vDB[i].getNombre());
            }
        } catch (Exception ex) {
            Logger.getLogger(SeleccionarProveedor.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally
        {
            try {
                pg.disconnect();
            } catch (SQLException ex) {
                Logger.getLogger(SeleccionarProveedor.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

