    class ProveedorXMateriaPrimaTableModel extends AbstractTableModel
    {
        private String[] columnNames = {"Nro",
                                        "Razon Social",
                                        "IVA",
                                        "Provincia",
                                        "Teléfono",
                                        "Mail",
                                        "Responsable",
                                        "Precio"};

        public Object getValueAt(int rowIndex, int columnIndex)
        {

            ViewProveedorXMateriaPrima view=null;
            view=filasProveedorXMateriaPrima.get(rowIndex);
//            try
//            {
//                view=filasProcesoCalidadXPiezaPresupuesto.get(rowIndex);
//            }
//            catch(Exception ex)
//            {
//                return "";
//            }
    //      Object[] df=filas.get(rowIndex);
            switch(columnIndex)
            {
            case 0:
              return NumerosAMostrar.getNumeroString(NumerosAMostrar.NRO_PROVEEDOR, view.getNroproveedor());
            case 1:
              return view.getRazonsocial();
            case 2:
              return view.getCondicioniva();
            case 3:
              return view.getProvincia();
            case 4:
              return view.getTelefono();
            case 5:
              return view.getMail();
            case 6:
              return view.getResponsable();
            case 7:
              return "$ "+view.getPrecio();
            default:
              return null;
            }

        }

        /**
         * Retorna la cantidad de columnas que tiene la tabla
         * @return Numero de filas que contendra la tabla
         */
        public int getColumnCount()
        {
          return columnNames.length;
        }

        public int getRowCount()
        {
          if(filasProveedorXMateriaPrima!=null)
            return filasProveedorXMateriaPrima.size();
          return 0;
        }

        /**
         * Devuelve el nombre de las columnas para mostrar en el encabezado
         * @param column Numero de la columna cuyo nombre se quiere
         * @return Nombre de la columna
         */

        @Override
        public String getColumnName(int column)
        {
          return columnNames[column];

        }
    }
