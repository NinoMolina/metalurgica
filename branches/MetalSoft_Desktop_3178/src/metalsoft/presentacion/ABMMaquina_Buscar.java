    private void txtValorKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtValorKeyReleased
        if(txtValor.getText().compareTo("")!=0) {
            final ABMMaquina_Buscar abm=this;
            timer=new Timer();
            timer.schedule(new TimerTask() {
                private HiloBuscarMaquina hiloBuscar;
                @Override
                public void run() {
                    hiloBuscar=new HiloBuscarMaquina();
                    hiloBuscar.setVentana(abm);
                    hiloBuscar.setValor(txtValor.getText());
                    hiloBuscar.start();
                }
            }, 1500);
        }
}//GEN-LAST:event_txtValorKeyReleased

    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed
        long id=Long.parseLong(((ItemCombo)lstLista.getSelectedValue()).getId());
        ventana.setIdMaquina(id);
        ventana.maquinaSeleccionada();
        dispose();
}//GEN-LAST:event_btnSeleccionarActionPerformed

