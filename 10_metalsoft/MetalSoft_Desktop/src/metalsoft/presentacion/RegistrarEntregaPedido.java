    private void btnRegistrarEntregaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarEntregaActionPerformed
        // TODO add your handling code here:
        int result = -1;
        PedidoDB db = gestor.buscarPedidoPorID(idPedido);
        db.setEstado(IdsEstadoPedido.ENTREGADO);
        db.setFechaentregareal(Fecha.parseToDateSQL(Fecha.parseToDate(Fecha.fechaActual())));
        result = gestor.updatePedido(db);
        int okRemito = -1;
        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Se registró la entrega del Pedido nro " + db.getNropedido() + ", en la fecha " + Fecha.fechaActual());

            okRemito = JOptionPane.showConfirmDialog(this, "Desea imprimir el Remito?");
            if (okRemito == JOptionPane.OK_OPTION) {
                imprimirRemito();
                try {
                    ImprimirFacturaOpciones facturaOpciones = new ImprimirFacturaOpciones(this);
                    facturaOpciones.setVentana(this);
                    PedidoJpaController controller = new PedidoJpaController(JpaUtil.getEntityManagerFactory());
                    Pedido p = controller.findPedido(idPedido);
                    facturaOpciones.setTipoFactura(p);
                    JFrameManager.centrarYMostrarVentana(facturaOpciones);
                    imprimirFactura();
                } catch (Exception ex) {
                    Logger.getLogger(RegistrarEntregaPedido.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo registrar la entrega del Pedido!");
        }
        pedidoSeleccionado(idPedido);
        btnRegistrarEntrega.setEnabled(false);
    }//GEN-LAST:event_btnRegistrarEntregaActionPerformed

    public static void setearCamposFactura(long idformapago, String tipo, Date fechaVencimiento) {
        formaPago = idformapago;
        tipoFactura = tipo;
        fechaVencimientoFactura = fechaVencimiento;
    }

    private void imprimirFactura() {
        double monto = 0d;
        for (ViewPresupuestoParaFactura de : filasDetalle) {
            monto += de.getPrecio();
        }
        gestor.imprimirFactura(idPedido, formaPago, tipoFactura, fechaVencimientoFactura, monto);
    }

    private void imprimirRemito() {
        gestor.imprimirRemito(idPedido);
    }

