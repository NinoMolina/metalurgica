    private void cmbprocesosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbprocesosActionPerformed
        if (ventana != null) {
            if (cmbprocesos.getSelectedIndex() > -1) {
                Etapadeproduccion etapa = gestorTrabajo.buscarEtapa(Long.parseLong(((ItemCombo)cmbprocesos.getSelectedItem()).getId()));
                ventana.setEtapa(etapa);
                btnaceptar.setEnabled(true);
            } else {
                btnaceptar.setEnabled(false);
                ventana.setEtapa(null);
            }
        }

    }//GEN-LAST:event_cmbprocesosActionPerformed

