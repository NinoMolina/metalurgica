    private void txtProductoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtProductoKeyReleased
        
     try{     
        
    
    
       String palabra=ValText.getPrimerCrtMayuscula(this.txtProducto.getText().trim());   
       this.ubicarPalabra(palabra);
      
   }
   catch(Exception ex){
      this.tblDetalle.setRowSelectionInterval(0,0);   
   }
        
      
        
    }//GEN-LAST:event_txtProductoKeyReleased
    
    private boolean ubicarPalabra(String palabra){
    
       char palabraV[]=palabra.toUpperCase().toCharArray();
        
        int cantPalabras=this.dtmDetalles.getRowCount();
        int tamPalabraV=palabraV.length;
        
        
        for(int i=0;i<cantPalabras;i++){
        
  
         String valorList=(String)this.tblDetalle.getValueAt(i,1);
         String valorListaMay=valorList.toUpperCase();
         
         char valorListV[]=valorListaMay.toCharArray();
         
         int j=0;
         if(valorListV.length>=tamPalabraV){
             for(j=0;j<tamPalabraV && palabraV[j]==valorListV[j];j++);
         }
         
        if(j==tamPalabraV){
          this.tblDetalle.setRowSelectionInterval(i,i);   
          
          return true;   
        }
         
        }
        
        return false;
    } 
    
    public void abrirVentana(PedidoAProveedor pedido,GestorConsultarPedidoProveedor gestor){
    
         
     this.gestor=gestor;
     this.tblDetalle.setModel(dtmDetalles);
    
     Pantalla.centrarEnPantallaJDialog(this);
     this.cargarDetalles(pedido.getMontoTotal(),pedido.getDetallesPedidoAProveedor());
     
  
     this.setModal(true);
     this.setVisible(true);
        
        
    }
    
    private void cargarDetalles(double montoTotal,Lista detalles){
     Iterador itDetalles=detalles.crearIterador();
     
     int fila=0;
     this.dtmDetalles.setRowCount(itDetalles.getCantidadElementos());
     
     while(itDetalles.siguiente()){
      
         DetallePedidoAProveedor detalle=(DetallePedidoAProveedor) itDetalles.getElementoActual();
         
         this.tblDetalle.setValueAt(new Integer(fila+1),fila,0);
         this.tblDetalle.setValueAt(detalle.getProducto().getNombre(), fila,1);
         this.tblDetalle.setValueAt(detalle.getEstado().getNombre(), fila,2);
         this.tblDetalle.setValueAt(new Double(detalle.getCantidad()),fila,3);
         this.tblDetalle.setValueAt(new Double(detalle.getPrecioActual()),fila,4);
         this.tblDetalle.setValueAt(new Double(detalle.getPrecioTotal()),fila,5);
      fila++;   
     }
     
     this.tblDetalle.setShowGrid(true);
     this.lblValorMontoTotal.setText(ValText.getStringDouble(montoTotal));
    }
  
