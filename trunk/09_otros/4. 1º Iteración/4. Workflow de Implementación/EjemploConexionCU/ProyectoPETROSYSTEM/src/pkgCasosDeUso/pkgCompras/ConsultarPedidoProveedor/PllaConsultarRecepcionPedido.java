    private void txtProductoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtProductoKeyReleased
        
     try{     
       
       String palabra=ValText.getPrimerCrtMayuscula(this.txtProducto.getText().trim());   
       this.ubicarPalabra(palabra);
      
     }
     catch(Exception ex){
       this.tblDetalleRecepcion.setRowSelectionInterval(0,0);   
     }
           
    }//GEN-LAST:event_txtProductoKeyReleased
    
       private boolean ubicarPalabra(String palabra){
    
       char palabraV[]=palabra.toUpperCase().toCharArray();
        
        int cantPalabras=this.dtmDetalles.getRowCount();
        int tamPalabraV=palabraV.length;
        
        
        for(int i=0;i<cantPalabras;i++){
        
  
         String valorList=(String)this.tblDetalleRecepcion.getValueAt(i,1);
         String valorListaMay=valorList.toUpperCase();
         
         char valorListV[]=valorListaMay.toCharArray();
         
         int j=0;
         if(valorListV.length>=tamPalabraV){
             for(j=0;j<tamPalabraV && palabraV[j]==valorListV[j];j++);
         }
         
        if(j==tamPalabraV){
          this.tblDetalleRecepcion.setRowSelectionInterval(i,i);   
          
          return true;   
        }
         
        }
        
        return false;
    } 
    public void abrirVentana(Recepcion r,GestorConsultarPedidoProveedor gestor){
      this.gestor=gestor;
      
      Pantalla.centrarEnPantallaJDialog(this);
    
      this.cargarDatosRecepcion(r);
      this.lblValorMontoTotal.setText(ValText.getStringDouble(r.getMontoTotal()));
      
 
      this.setModal(true);
      this.setVisible(true);
    
    }
    
    private void cargarDatosRecepcion(Recepcion recepcion){
    
     this.lblValorFechaRecepcion.setText(ValText.getFormatStringFecha(recepcion.getFechaRealRecepcion()));     
     this.lblValorHoraRecepcion.setText(recepcion.getHoraRealRecepcion());
     if(recepcion.getFactura()!=null){
      this.lblValorNroFactura.setText(ValText.getStringInt(recepcion.getFactura().getNumero()));  
     }
     
     this.cargarRemitos(recepcion.getRemito());
     
     this.cargarDetalles(recepcion.getDetallesRecepcion());
     
     
    }
    
    private void cargarRemitos(Lista remitos){
      Iterador itRemitos=remitos.crearIterador();
      
      while(itRemitos.siguiente()){
        Remito r=(Remito) itRemitos.getElementoActual();
        this.cmbNroRemito.addItem(new Integer(r.getNumero()));
      }
    }
    
    private void cargarDetalles(Lista detalles){
     Iterador itDetalles=detalles.crearIterador();
     
     int fila=0;
     this.dtmDetalles.setRowCount(itDetalles.getCantidadElementos());
     
     
     while(itDetalles.siguiente()){
      
         DetalleRecepcion detalle=(DetalleRecepcion) itDetalles.getElementoActual();
         
         this.tblDetalleRecepcion.setValueAt(new Integer(fila+1),fila,0);
         this.tblDetalleRecepcion.setValueAt(detalle.getProducto().getNombre(), fila,1);
         this.tblDetalleRecepcion.setValueAt(ValText.getFormatStringFecha(detalle.getFechaRecepcion()), fila,2);
         this.tblDetalleRecepcion.setValueAt(detalle.getHoraRealRecepcion(),fila,3);
         this.tblDetalleRecepcion.setValueAt(new Double(detalle.getCantidadRecibida()),fila,4);
         this.tblDetalleRecepcion.setValueAt(new Double(detalle.getPrecioUnitario()),fila,5);
         this.tblDetalleRecepcion.setValueAt(new Double(detalle.getMontoTotal()),fila,6);
         
         fila++;   
     }
     
     this.tblDetalleRecepcion.setShowGrid(true);
       
    }
    
    
