    class ServicioTableModel extends AbstractTableModel {

        String[] columnName = {"Nombre", "Descripción", "Duración", "Observaciones"};

        public int getRowCount() {
            if (filasDetalle != null) {
                return filasDetalle.size();
            }
            return 0;
        }

        public int getColumnCount() {
            return columnName.length;
        }

        public Object getValueAt(int rowIndex, int columnIndex) {
            Detallemantenimientocorrectivo detalle = filasDetalle.get(rowIndex);
            switch (columnIndex) {
                case 0:
                    return detalle.getRotura().getNombre();
                case 1:
                    return detalle.getRotura().getDescripcion();
                case 2:
                    return String.valueOf(detalle.getDuracion());
                case 3:
                    return detalle.getMotivorotura();
                default:
                    return null;
            }

        }

        @Override
        public String getColumnName(int column) {
            return columnName[column];
        }
    }
