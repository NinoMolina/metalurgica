    public boolean validarDatos() {
        boolean result = true;
        if (cmbTipoMaquina.getSelectedIndex() < 0) {
            result = false;
        }
        if (cmbMaquina.getSelectedIndex() < 0) {
            result = false;
        }

        if (tblDetalle.getRowCount() <= 0) {
            result = false;
        }

        if (cmbEmpleados.getSelectedIndex() < 0) {
            result = false;
        }

        if (cmbProveedores.getSelectedIndex() < 0) {
            result = false;
        }

        return result;
    }

